#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*tmp;

	if (s != NULL)
	{
		i = -1;
		tmp = (char*)s;
		while (++i == 0 || tmp[i - 1])
			if (tmp[i] == c)
				return (&tmp[i]);
	}
	return (NULL);
}
