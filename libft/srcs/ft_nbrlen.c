#include "libft.h"

size_t	ft_nbrlen(int nb)
{
	size_t	nb_len;

	nb_len = 0;
	if (nb <= 0)
		nb_len++;
	if (nb < 0)
		nb *= -1;
	while (nb > 0)
	{
		nb /= 10;
		nb_len++;
	}
	return (nb_len);
}
