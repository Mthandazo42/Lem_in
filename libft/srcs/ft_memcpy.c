#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t i;

	i = 0;
	while (++i <= n)
		((char*)dest)[i - 1] = ((char*)src)[i - 1];
	return (dest);
}
